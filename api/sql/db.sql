CREATE TABLE IF NOT EXISTS projects(
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL CHECK (name <> ''),
    description text NOT NULL,
    date date NOT NULL,
    github text,
    deploy text,
    photo text
);

